"""
Take 3 input from the user
perform sum, sub, mul, div
"""

num1 = float(input("Enter first number: "))
num2 = float(input("Enter second number: "))
num3 = float(input("Enter third number: "))

sum = num1 + num2 + num3
sub = num1 - num2 - num3
mul = num1 * num2 * num3
div = num1 / num2 / num3
print(sum)
print(sub)
print(mul)
print(div)

